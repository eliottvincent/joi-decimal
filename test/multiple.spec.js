const { expect } = require('chai');

const BaseJoi = require('@hapi/joi');
const Decimal = require('decimal.js');
const DecimalExtension = require('..');

const Joi = BaseJoi.extend(DecimalExtension);

const shouldSucceed = (value, base) => {
  it('should validate if value is a multiple of base', () => {
    const decValidator = Joi.decimal().multiple(base);

    const result = decValidator.validate(value);

    expect(result.error).to.be.null;
  });
};

const shouldFail = (value, base) => {
  it('should return a validation error if value is not a multiple of base', () => {
    const decValidator = Joi.decimal().multiple(base);

    const result = decValidator.validate(value);

    expect(result.error).to.not.be.null;
    expect(result.error.name).to.be.equal('ValidationError');
    expect(result.error.message).to.match(/is not multiple of base/);
  });
};

const shouldFailSdRm = (value, base, sd, rm) => {
  it('should return a validation error if value is not a multiple of base', () => {
    Decimal.precision = sd;
    Decimal.rounding = rm;
    const decValidator = Joi.decimal().multiple(base);

    const result = decValidator.validate(value);

    expect(result.error).to.not.be.null;
    expect(result.error.name).to.be.equal('ValidationError');
    expect(result.error.message).to.match(/is not multiple of base/);
  });
};


